module SlotsHelper

  def month_string(num)
    if num == 1
      "January"
    elsif num == 2
      "February"
    elsif num == 3
      "March"
    elsif num == 4
      "April"
    elsif num == 5
      "May"
    elsif num == 6
      "June"
    elsif num == 7
      "Juli"
    elsif num == 8
      "August"
    elsif num == 9
      "September"
    elsif num == 10
      "October"
    elsif num == 11
      "November"
    elsif num == 12
      "December"
    end
  end
end
